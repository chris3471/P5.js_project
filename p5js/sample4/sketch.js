function setup() {
  createCanvas(600, 400);
  frameRate(30);
}
function draw() {
  colorMode(HSB, 360, 100, 100, 255);
  let skyHue = 130 + sin(frameCount * 0.03) * 40;
  let skyBright = 15 + sin(frameCount * 0.03) * 8;
  background(skyHue, 80, skyBright);
  colorMode(RGB, 255);
  noStroke();
  let starAlpha = 180 + sin(frameCount * 0.05) * 40;
  fill(255, 255, 255, starAlpha);
  ellipse(45, 50, 5, 5);
  fill(255, 255, 255, starAlpha * 0.85);
  ellipse(175, 35, 5, 5);
  let sa3 = starAlpha * 1.1; if (sa3 > 255) sa3 = 255;
  fill(255, 255, 255, sa3);
  ellipse(430, 40, 5, 5);
  fill(255, 255, 255, starAlpha * 0.9);
  ellipse(565, 60, 5, 5);
  let moonBright = 190 + sin(frameCount * 0.02) * 20;
  noStroke();
  fill(210, 185, 120, moonBright);
  ellipse(500, 90, 100, 100);
  fill(255, 240, 210, 45);
  ellipse(480, 75, 55, 55);
  fill(90, 95, 110, 50);
  ellipse(505, 120, 125, 30);
  fill(60, 55, 60);
  rect(0, 280, 600, 120);
  stroke(240, 205, 120, 180);
  strokeWeight(4);
  line(65, 345, 145, 345);
  line(180, 345, 260, 345);
  line(295, 345, 375, 345);
  line(410, 345, 490, 345);
  let h1 = sin(frameCount * 0.07) * 6;
  let h2 = sin(frameCount * 0.07 + 1.2) * 6;
  let h3 = sin(frameCount * 0.07 + 2.4) * 6;
  let w1 = cos(frameCount * 0.05) * 4;
  let w2 = cos(frameCount * 0.05 + 1.0) * 4;
  let w3 = cos(frameCount * 0.05 + 2.0) * 4;
  noStroke();
  fill(20, 20, 30, 210);
  ellipse(110 + w1, 300, map(h1, -6, 6, 160, 220), 50);
  ellipse(325 + w2, 310, map(h2, -6, 6, 195, 255), 55);
  ellipse(520 + w3, 300, map(h3, -6, 6, 145, 205), 45);
  noStroke(); fill(180, 180, 190);
  ellipse(120 + w1, 95 + h1, 45, 20);
  fill(90, 90, 100);
  ellipse(105 + w1, 95 + h1, 10, 10);
  ellipse(135 + w1, 95 + h1, 10, 10);
  stroke(230); strokeWeight(2);
  line(100+w1, 90+h1, 80+w1,  80+h1); line(140+w1, 90+h1, 160+w1, 80+h1);
  line(80+w1,  80+h1, 65+w1,  85+h1); line(160+w1, 80+h1, 175+w1, 85+h1);
  noStroke(); fill(180, 180, 190);
  ellipse(360 + w2, 75 + h2, 45, 20);
  fill(90, 90, 100);
  ellipse(345+w2, 75+h2, 10, 10); ellipse(375+w2, 75+h2, 10, 10);
  stroke(230);
  line(340+w2, 70+h2, 320+w2, 60+h2); line(380+w2, 70+h2, 400+w2, 60+h2);
  line(320+w2, 60+h2, 305+w2, 65+h2); line(400+w2, 60+h2, 415+w2, 65+h2);
  noStroke(); fill(180, 180, 190);
  ellipse(525 + w3, 115 + h3, 45, 20);
  fill(90, 90, 100);
  ellipse(510+w3, 115+h3, 10, 10); ellipse(540+w3, 115+h3, 10, 10);
  stroke(230);
  line(505+w3, 110+h3, 485+w3, 100+h3); line(545+w3, 110+h3, 565+w3, 100+h3);
  line(485+w3, 100+h3, 470+w3, 105+h3); line(565+w3, 100+h3, 580+w3, 105+h3);
  strokeWeight(5);
  stroke(255, 70, 70, 180 + sin(frameCount * 0.15) * 75);
  line(120+w1, 100+h1, 265, 220);
  stroke(255, 210, 60, 180 + sin(frameCount * 0.15 + 1.0) * 75);
  line(360+w2, 80+h2, 265, 220);
  stroke(40, 230, 255, 180 + sin(frameCount * 0.15 + 2.0) * 75);
  line(525+w3, 120+h3, 265, 220);
  let expScale = 1.0 + sin(frameCount * 0.12) * 0.18;
  let c1 = lerpColor(color(255, 100, 50, 150), color(255, 60, 0, 120), (sin(frameCount * 0.1)+1)/2);
  let c2 = lerpColor(color(255, 190, 85, 170), color(255, 140, 30, 150), (sin(frameCount * 0.1)+1)/2);
  noStroke();
  fill(c1); ellipse(265, 220, 85 * expScale, 85 * expScale);
  fill(c2); ellipse(265, 220, 50 * expScale, 50 * expScale);
  fill(255, 245, 190, 210); ellipse(265, 220, 20 * expScale, 20 * expScale);
}
